int	ft_str_is_lowercase(char *str)
{
	char	c;
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!(c <= 'z' && c >= 'a'))
			return (0);
		i++;
	}
	return (1);
}
