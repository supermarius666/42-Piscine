int	ft_str_is_numeric(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!(c <= '9' && c >= '0'))
			return (0);
		i++;
	}
	return (1);
}
