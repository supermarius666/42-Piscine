int	ft_sqrt(int nb)
{
	int	res;

	res = 1;
	if (nb <= 0)
		return (0);
	while ((res * res) < nb && res <= 46340)
		res++;
	if ((res * res) == nb)
		return (res);
	else
		return (0);
}
